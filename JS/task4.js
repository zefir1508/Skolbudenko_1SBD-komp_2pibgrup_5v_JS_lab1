function найбільшийСпільнийДільник(a, b) {
    // Перевірка на неправильні вхідні значення
    if (a < 20 || a > 40 || b < 20 || b > 40) {
        console.log("Введені числа повинні бути в діапазоні від 20 до 40");
        return;
    }

    // Алгоритм Евкліда для знаходження НСД
    while (b !== 0) {
        let тимчасовий = b;
        b = a % b;
        a = тимчасовий;
    }

    // a тепер містить НСД
    console.log("Найбільший спільний дільник:", a);
}

// Приклад виклику функції для чисел 25 і 35
найбільшийСпільнийДільник(25, 35);
