// Функція для обчислення кількості двозначних чисел у послідовності
function кількістьДвозначних(послідовність) {
    let кількість = 0; // Змінна для збереження кількості двозначних чисел
    
    // Проходимо по кожному елементу у послідовності
    for (let число of послідовність) {
        // Перевіряємо, чи є поточне число двозначним
        if (число >= 10 && число <= 99) {
            кількість++; // Якщо так, збільшуємо лічильник
        }
    }
    
    return кількість; // Повертаємо кількість двозначних чисел
}

// Послідовність чисел, яка вводиться користувачем (для прикладу використовується статичний масив)
const послідовність = [15, 7, 42, 101, 56, 23, 89, 37];

// Викликаємо функцію та виводимо результат
const результат = кількістьДвозначних(послідовність);
console.log("Кількість двозначних чисел у послідовності:", результат);
